export function folderIcon(name: string, isOpen: boolean): string {
  if (isOpen) return folderUrl('folder-open')
  const key = name.toLowerCase()
  const mapped = FOLDER_BY_NAME[key]
  return folderUrl(mapped ?? 'folder')
}

export function fileIcon(name: string): string {
  const lower = name.toLowerCase()

  if (lower === 'crab.md') return asset('sreda.png')

  if (FILE_BY_NAME[lower]) return fileUrl(FILE_BY_NAME[lower])

  const parts = lower.split('.')

  if (parts.length >= 3) {
    const compound = parts.slice(-2).join('.')
    if (FILE_BY_EXT[compound]) return fileUrl(FILE_BY_EXT[compound])
  }

  const ext = parts.length > 1 ? parts[parts.length - 1] : ''
  if (ext && FILE_BY_EXT[ext]) return fileUrl(FILE_BY_EXT[ext])

  return fileUrl('document')
}
