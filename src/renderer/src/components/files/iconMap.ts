const FILE_BASE = 'img'
const FOLDER_BASE = 'floder'

function fileUrl(value: string): string {
  if (value.includes('.')) return asset(`${FILE_BASE}/${value}`)
  return asset(`${FILE_BASE}/${value}.svg`)
}
function folderUrl(value: string): string {
  if (value.includes('.')) return asset(`${FOLDER_BASE}/${value}`)
  return asset(`${FOLDER_BASE}/${value}.svg`)
}


